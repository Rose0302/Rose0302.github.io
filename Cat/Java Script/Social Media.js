let facebookIcon = document.querySelector('#facebook-icon');
let instagramIcon = document.querySelector('#instagram-icon');
     
facebookIcon.addEventListener('mouseover', function() {
facebookIcon.style.fontSize = "50px";
});
facebookIcon.addEventListener('mouseout', function() {
facebookIcon.style.fontSize = "";
});
     
instagramIcon.addEventListener('mouseover', function() {
instagramIcon.style.fontSize = "50px";
});
instagramIcon.addEventListener('mouseout', function() {
instagramIcon.style.fontSize = "";
});